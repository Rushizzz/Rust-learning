fn main() {
    let _logical : bool = true;
    let _float : f32 = 1.22;
    let _integer: i32 = 100;
    println!("logical = {_logical} \nfloat = {_float} \ninteger = {_integer} ");

    let mut something : i32 = 1000;
    println!("something before = {something}");
    something = 41;
    println!("something after = {something}");

    //this is string 
    let _string: &str = "Rushikesh is my name";
    println!("string = {_string}");
}


